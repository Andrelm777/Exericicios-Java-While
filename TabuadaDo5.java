package main;

public class TabuadaDo5 {

    public static void main(String[] args) {
        // Imprimir a tabuada do 5 (de 1 a 10)

        int contador = 1;

        while (contador <= 10) {
            System.out.println("5 x " + contador + " = " + (5 * contador));
            contador++;
        }
    }
}