package main;

public class TabuadaDo7 {

	public static void main(String[] args) {
		// Imprimir a tabuada do 7 (de 1 a 10)
		
		 int contador = 1;

	        while (contador <= 10) {
	            System.out.println("7 x " + contador + " = " + (7 * contador));
	            contador++;
	        }

	}

}
