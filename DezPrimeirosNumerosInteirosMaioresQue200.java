package main;

public class DezPrimeirosNumerosInteirosMaioresQue200 {

	public static void main(String[] args) {
		// Imprimir os 10 primeiros números inteiros maiores que 200
		int contador= 200;
		
		while (contador <= 209) {
			System.out.println(contador);
			contador ++;
			
		}
	}

}
